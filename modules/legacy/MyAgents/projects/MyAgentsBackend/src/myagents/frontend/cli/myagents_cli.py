def cmd_rebuild(args):
    """Rebuild and reinstall myagents package.

    Works from any directory.
    """
    source_root = project_dir
    print(f"Rebuilding myagents package from source: {source_root}")

    try:
        # Clean previous build artifacts
        build_dir = source_root / "build"
        dist_dir = source_root / "dist"
        egg_dir = source_root / "myagents.egg-info"

        print("Cleaning previous build artifacts...")
        for dir_path in [build_dir, dist_dir, egg_dir]:
            if dir_path.exists():
                subprocess.run(
                    ["rm", "-rf", str(dir_path)],
                    check=True,
                    capture_output=True
                )

        # Build the package
        print("Building package...")
        env = os.environ.copy()
        env["PIP_NO_INPUT"] = "1"  # Prevent pip from prompting for credentials
        build_result = subprocess.run(
            ["python", "-m", "build", "--installer", "uv"],
            cwd=str(source_root),
            capture_output=True,
            text=True,
            env=env
        )

        if build_result.returncode != 0:
            print("Error building package:", file=sys.stderr)
            if build_result.stderr:
                print(build_result.stderr, file=sys.stderr)
            sys.exit(1)

        # Reinstall the package
        print("Reinstalling package...")
        install_result = subprocess.run(
            ["uv", "sync"],
            cwd=str(source_root),
            capture_output=True,
            text=True,
            env=env
        )

        if install_result.returncode == 0:
            print("Build complete")
            print("Successfully rebuilt and reinstalled myagents")
            if install_result.stdout:
                print(install_result.stdout)
            sys.exit(0)
        else:
            print("Error reinstalling myagents:", file=sys.stderr)
            if install_result.stderr:
                print(install_result.stderr, file=sys.stderr)
            sys.exit(1)

    except FileNotFoundError as e:
        print(f"Error: Required tool not found: {e}", file=sys.stderr)
        print("Please ensure python build tools are available", file=sys.stderr)
        sys.exit(1)
    except Exception as e:
        print(f"Error: {e}", file=sys.stderr)
        sys.exit(1)


