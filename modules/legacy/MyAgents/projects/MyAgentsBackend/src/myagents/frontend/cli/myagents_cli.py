def _write_env_file_for_chat(project_root: Path) -> None:
    """Write .env file for LangGraph chat session compatibility.

    Auto-generates .env file from gcptoolkit secrets. This ensures compatibility
    with LangGraph runtime which may check for .env file.

    Args:
        project_root: Path to project root directory (unused, kept for compatibility)

    Note:
        - File is written to ~/.config/myagents/.env
        - GEMINI_API_KEY is mapped to GOOGLE_API_KEY (LangGraph expects this name)
        - Errors are logged but don't fail the operation (subprocess env still works)
    """
    from myagents.backend.services.agents.domains.secrets import get_secret

    config_dir = Path.home() / ".config" / "myagents"
    config_dir.mkdir(parents=True, exist_ok=True)
    env_file_path = config_dir / ".env"

    try:
        # Get secrets from gcptoolkit
        gemini_key = get_secret("GEMINI_API_KEY")
        langsmith_key = get_secret("LANGSMITH_API_KEY")

        # Build .env file contents
        lines = [
            "# Auto-generated by MyAgents - DO NOT EDIT",
            "# This file is automatically generated from gcptoolkit secrets",
            "# Changes will be overwritten on next chat session",
            "",
        ]

        # Track which keys were written
        keys_written = []

        # Write required keys for LangGraph
        # Note: Use GOOGLE_API_KEY (not GEMINI_API_KEY) as LangGraph expects this name
        if gemini_key:
            lines.append(f"GOOGLE_API_KEY={gemini_key}")
            keys_written.append("GOOGLE_API_KEY")
        if langsmith_key:
            lines.append(f"LANGSMITH_API_KEY={langsmith_key}")
            keys_written.append("LANGSMITH_API_KEY")

        # Add LangSmith tracing configuration
        lines.append("LANGSMITH_TRACING=true")
        lines.append("LANGSMITH_PROJECT=myagents-cli-chat")

        # Write to file
        env_file_path.write_text("\n".join(lines) + "\n")

        # Show success message with keys written
        print("✓ .env file generated successfully")
        if keys_written:
            print(f"  Keys configured: {', '.join(keys_written)}")

    except Exception as e:
        # Log warning but don't fail - subprocess environment still works via gcptoolkit
        print(f"⚠ Warning: Failed to write .env file: {e}")
        print("  Chat will still work via gcptoolkit secrets in subprocess environment")


def cmd_agent_chat(args):
    """Run interactive agent chat.

    Works from any directory using home directory configuration.

    Args:
        args: Parsed command-line arguments
            - agent: Agent type to use (any registered agent type)

    Behavior:
        - Checks for gcptoolkit config (REQUIRED - fails if missing)
        - Loads API key from GCP Secret Manager
        - Auto-generates .env file for LangGraph compatibility
        - Checks Studio status and displays connection info
        - Starts interactive chat loop
        - Supports 'quit', 'exit', 'q' to exit
    """
    # Check for gcptoolkit config BEFORE any other operations
    from myagents.backend.services.agents.domains.config_checker import ensure_config_or_exit
    ensure_config_or_exit()

    # Lazy imports
    import os
    from myagents.backend.services.agents.workflows.agent_registry import (
        get_agent_runner,
        get_agent_description,
    )
    from myagents.backend.services.agents.domains.secrets import get_secret
    from myagents.backend.services.agents.workflows.studio_workflow import StudioWorkflow

    # Determine which agent to use
    agent_type = args.agent or "builder"

    # Get the agent runner dynamically from registry
    try:
        run_agent = get_agent_runner(agent_type)
        agent_desc = get_agent_description(agent_type)
    except ValueError as e:
        print(f"Error: {e}", file=sys.stderr)
        sys.exit(1)

    # Set up API key
    api_key = get_secret("GEMINI_API_KEY")
    os.environ["GOOGLE_API_KEY"] = api_key

    # Auto-generate .env file for LangGraph compatibility
    _write_env_file_for_chat(project_dir)

    # Get Studio status using StudioWorkflow
    try:
        workflow = StudioWorkflow(home_config_dir=None, config_path=config_path)
        studio_status = workflow.get_studio_status()
    except RuntimeError:
        # If secrets are not available, continue without studio status
        studio_status = {"running": False}

    print("=" * 60)
    print(f"MyAgents CLI - {agent_type} Agent")
    print(f"  {agent_desc}")
    print("=" * 60)

    if studio_status["running"]:
        print(f"[Studio] Running on port {studio_status['port']}")
        print(f"[Studio] WebUI: {studio_status['url']}")
    else:
        print("[Studio] Not running (start with: myagents studio start)")

    print("=" * 60)
    print("Type your messages below. Type 'quit' or 'exit' to end.")
    print("=" * 60)
    print()

    # Persistent state across turns
    state = None

    while True:
        try:
            user_input = input("\nYou: ").strip()

            if user_input.lower() in ['quit', 'exit', 'q']:
                print("\nGoodbye!")
                break

            if not user_input:
                continue

            print("\nAgent: ", end="", flush=True)
            try:
                response, state = run_agent(user_input, state)
                print(response)
            except Exception as e:
                print(f"\n[ERROR] Failed to get response: {e}")

        except KeyboardInterrupt:
            print("\n\nInterrupted. Goodbye!")
            break
        except EOFError:
            print("\n\nGoodbye!")
            break


def cmd_agents_list(args):
    """List all available agents in the registry.

    Shows agent names and descriptions.
    """
    from myagents.backend.services.agents.workflows.agent_registry import (
        list_agents,
        get_agent_description,
    )

    agents = list_agents()

    print("=" * 60)
    print("Available Agents")
    print("=" * 60)

    # Group agents by category
    categories = {
        "Chat Agents": ["builder", "coding"],
        "Cleaner Agents": ["cleaner-identify", "cleaner-execute"],
        "Test Agents": ["test-runner", "test-audit", "test-user-sim"],
        "Explore Agents": ["explore-architecture", "explore-feature",
                          "explore-dependency", "explore-test", "explore-synthesis"],
    }

    for category, category_agents in categories.items():
        available_in_category = [a for a in category_agents if a in agents]
        if available_in_category:
            print(f"\n{category}:")
            for agent in available_in_category:
                desc = get_agent_description(agent)
                print(f"  {agent:22} - {desc}")

    # Show any uncategorized agents
    all_categorized = set()
    for category_agents in categories.values():
        all_categorized.update(category_agents)

    uncategorized = [a for a in agents if a not in all_categorized]
    if uncategorized:
        print("\nOther Agents:")
        for agent in uncategorized:
            desc = get_agent_description(agent)
            print(f"  {agent:22} - {desc}")

    print()
    print("=" * 60)
    print("Usage: myagents chat --agent <AGENT>")
    print("       myagents clean --identify | --execute")
    print("       myagents test --run | --audit | --simulate")
    print("       myagents explore --arch | --feature | --dep | --test | --synth")
    print("=" * 60)


