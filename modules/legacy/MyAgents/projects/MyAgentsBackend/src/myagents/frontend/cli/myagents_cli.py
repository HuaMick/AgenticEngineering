def cmd_studio_start(args):
    """Start LangGraph Studio.

    Works from any directory using home directory configuration.
    Uses StudioWorkflow for all operations.

    Port resolution priority:
        1. --port flag (highest priority)
        2. studio.port preference
        3. config.yml server.port (lowest priority)

    Args:
        args: Parsed command-line arguments
            - port: Port for Studio server (default: 2024)
            - foreground: Run in foreground vs background (default: False)

    Behavior:
        - Starts LangGraph Studio process
        - Displays success message with URL or error details
        - Shows recent error logs if startup fails
        - Exits with code 0 on success, 1 on failure
    """
    # Lazy imports
    from myagents.backend.services.agents.workflows.studio_workflow import StudioWorkflow
    from myagents.backend.services.agents.workflows.setup_workflow import PreferencesWorkflow

    try:
        # Determine port: CLI flag > preference > config default (None)
        port = None

        if args.port is not None:
            # User explicitly provided --port flag (default is None)
            port = args.port
        else:
            # Check preferences for studio.port
            pref_workflow = PreferencesWorkflow()
            success, _, pref_value = pref_workflow.get_preference(key="studio.port")
            if success and pref_value is not None:
                try:
                    port = int(pref_value)
                except (ValueError, TypeError):
                    pass  # Invalid preference, use config default

        workflow = StudioWorkflow(home_config_dir=None, config_path=config_path)
        success, message = workflow.start_studio(background=not args.foreground, port=port)

        if success:
            print(message)
        else:
            # Display failure message
            print(message)

            # Try to retrieve and display recent errors from stderr log
            recent_errors = workflow.get_recent_errors(num_lines=20)

            if recent_errors:
                print("\nRecent errors from Studio logs:")
                print(recent_errors)

        sys.exit(0 if success else 1)
    except RuntimeError as e:
        print(f"Error: {e}", file=sys.stderr)
        sys.exit(1)


