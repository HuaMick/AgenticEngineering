def _get_gcptoolkit_root():
    """Find the Agent-GCPtoolkit repository root.

    Returns:
        Path: Path to Agent-GCPtoolkit directory

    Raises:
        RuntimeError: If Agent-GCPtoolkit cannot be found
    """
    # Try multiple locations
    possible_locations = [
        project_dir.parent / "Agent-GCPtoolkit" if project_dir else None,  # sibling directory (local dev)
        project_dir / "Agent-GCPtoolkit" if project_dir else None,  # inside workspace (Docker)
        Path.home() / "myagents" / "Agent-GCPtoolkit",  # home directory
        Path("/home/code/myagents/Agent-GCPtoolkit"),  # hardcoded fallback
    ]

    for location in possible_locations:
        if location and location.exists() and (location / "pyproject.toml").exists():
            # Verify it's actually agent-gcptoolkit by checking pyproject.toml
            import tomllib
            try:
                with open(location / "pyproject.toml", "rb") as f:
                    data = tomllib.load(f)
                    if data.get("project", {}).get("name") == "agent-gcptoolkit":
                        return location
            except Exception:
                continue

    raise RuntimeError("Agent-GCPtoolkit repository not found")


def cmd_gcptoolkit_build(args):
    """Build agent-gcptoolkit package."""
    import os
    import subprocess
    import shutil

    # Find gcptoolkit root
    try:
        gcptoolkit_root = _get_gcptoolkit_root()
    except RuntimeError as e:
        print(f"Error: {e}", file=sys.stderr)
        sys.exit(1)

    # Build the package using python -m build
    print(f"Building agent-gcptoolkit from {gcptoolkit_root}...")

    # Ensure build-artifacts/dist directory exists
    dist_dir = gcptoolkit_root / "build-artifacts" / "dist"

    # Clean previous build
    if dist_dir.exists():
        shutil.rmtree(dist_dir)
    dist_dir.mkdir(parents=True)

    # Build package
    env = os.environ.copy()
    env["PIP_NO_INPUT"] = "1"

    result = subprocess.run(
        ["python", "-m", "build", "--installer", "uv", "--outdir", str(dist_dir)],
        cwd=str(gcptoolkit_root),
        env=env
    )

    if result.returncode == 0:
        print("✓ Build complete")
        print(f"  Artifacts: {dist_dir}")
    else:
        print("✗ Build failed", file=sys.stderr)

    sys.exit(result.returncode)


def cmd_gcptoolkit_update(args):
    """Update agent-gcptoolkit package from latest build."""
    import os
    import subprocess

    # Find gcptoolkit root
    try:
        gcptoolkit_root = _get_gcptoolkit_root()
    except RuntimeError as e:
        print(f"Error: {e}", file=sys.stderr)
        sys.exit(1)

    # Find the latest wheel
    dist_dir = gcptoolkit_root / "build-artifacts" / "dist"
    if not dist_dir.exists():
        print("Error: No build artifacts found. Run 'myagents gcptoolkit build' first.", file=sys.stderr)
        sys.exit(1)

    wheels = list(dist_dir.glob("agent_gcptoolkit-*.whl"))
    if not wheels:
        print(f"Error: No wheel found in {dist_dir}. Run 'myagents gcptoolkit build' first.", file=sys.stderr)
        sys.exit(1)

    # Use the first wheel (should only be one)
    wheel = wheels[0]
    print(f"Updating agent-gcptoolkit from {wheel.name}...")

    # Install using uv pip (or fall back to regular pip)
    env = os.environ.copy()
    env["PIP_NO_INPUT"] = "1"

    # Use uv pip
    result = subprocess.run(
        ["uv", "pip", "install", "--force-reinstall", str(wheel)],
        env=env,
        capture_output=True
    )

    if result.returncode == 0:
        print("✓ Update complete")
    else:
        print("✗ Update failed", file=sys.stderr)

    sys.exit(result.returncode)


